<hr><h2>We can configure this plug-in in three different way.</h2>

<h2><?php echo wp_specialchars( '1.Drag and drop the widget!' ); ?></h2>
1.	Go to widget menu and drag and drop the "superb slideshow gallery" widget to your sidebar location. or <br />

<h2><?php echo wp_specialchars( '2.Paste the below code to your desired template location!' ); ?></h2>
2.	Copy and past the below mentioned code to your desired template location(ie in PHP file).
<div style="padding-top:7px;padding-bottom:7px;">
<code style="padding:7px;">
&lt;?php if (function_exists (ssg_show)) ssg_show(); ?&gt;
</code></div>

<h2><?php echo wp_specialchars( '3.Use below code in post or page!' ); ?></h2>
 3. Use below code in post or page.
<div style="padding-top:7px;padding-bottom:7px;">
<input name="" style="width:500px;height:25px;" value="[ssg-superb-slideshow=widget=250=200=2500=500=0=YES]" type="text" />
</div>

short code reference : [ssg-superb-slideshow=type=width=height=pause=fadeduration=cycles=random].<br /><br />
ssg-superb-slideshow = short code.<br />
type = Enter gallery type (This field is to group the images).<br />
width = Width of the gallery.<br />
height = Height of the gallery.<br />
pause = Pause between content change.<br />
fadeduration = The duration of the fade effect when transitioning from one image to the next, in milliseconds.<br />
random = galley image order.<br />

